                    <div class="content">
                        <p>
         The <strong>Elementary & Secondary Education Foundation (ESEF)</strong> is an autonomous body of the Government of Khyber Pakhtunkhwa, 
         aiming to reach every part of the province without access to educational facilities. In order to achieve the goal of 
         <em>"<strong>No more out of school</strong>",</em> ESEF has launched a range of programs to manage <strong>out-of-school children (OOSC)</strong> through non-formal 
         education methods. These programs include:
     </p>
     <ul>
         <li>Girl Community Schools (GCS)</li>
         <li>New School Initiative (NSI)</li>
         <li>Education Support Scheme (ESS)</li>
         <li>Participatory Online Home learning Alternatives (POHA)</li>
         <li>Other related initiatives</li>
     </ul>
     <p>
         All of these programs are designed to provide <strong>affordable access</strong> to children to help them get to school. 
         According to available data from <strong>BISP 2021-22</strong> and <strong>PIE 2022-23</strong>, more than <strong>4.9 million children</strong> 
         are out of school in Khyber Pakhtunkhwa, with <strong>82% of them</strong> recently crossing the age limit for admission into the 
         formal education system.
     </p>
     <p>
         To address this challenge and bring these children back to school, ESEF has initiated the <strong>Accelerated Learning Program (ALP)</strong>, 
         which provides a <em>second chance</em> for children to pursue education through a condensed curriculum. ALP enables students 
         to complete the <strong>national single curriculum</strong> in <em>half the actual time</em>, offering a <strong>flexible and inclusive study plan</strong> 
         that helps them overcome educational gaps.
     </p>
     <p>
         The curriculum is designed by considering various challenges such as <em>gender inequality, family obligations, poverty, strict schedules,</em> 
         and <em>education expenses</em>. This creative strategy ensures that <strong>underprivileged children, especially girls</strong>, have access to education 
         and a brighter future.
     </p>
     <p>
         The local community is actively involved in the establishment of <strong>ALP centers</strong> through a community committee. The committee 
         helps by:
     </p>
     <ul>
         <li>Providing free space with necessary facilities</li>
         <li>Selecting teachers and facilitators from local female graduates</li>
         <li>Identifying out-of-school children in the area</li>
     </ul>
     <p>
         ESEF has made all the <strong>Accelerated Learning Program (ALP) resources</strong> available online on its official website for the 
         benefit of other implementing partners and individuals. Additionally, a broad-based <strong>"Technical Working Committee of NFE"</strong> 
         has been formed by the E&SE Department to ensure the project's success and to resolve planning and execution challenges.
     </p>
     <p>
         This committee includes:
     </p>
     <ul>
         <li>Government agencies</li>
         <li>Donors</li>
         <li>National and international NGOs</li>
         <li>Other implementing partners</li>
     </ul>
